class BodyParts {
    torso;
    leftHand;
    rightHand;
    leftLeg;
    rightLeg;

    constructor(torso, leftHand, rightHand, leftLeg, rightLeg) {
        this.torso = torso;
        this.leftHand = leftHand;
        this.rightHand = rightHand;
        this.leftLeg = leftLeg;
        this.rightLeg = rightLeg;
    }
}

export {BodyParts};